
export const translate = (country) => {
  switch (country) {
    case 'brazil':
      return 'Brasil'
    case 'costa rica':
      return 'Costa Rica'
    case 'japan':
      return 'Japão'
    case 'canada':
      return 'Canadá'
    case 'united states':
      return 'E.U.A'
    case 'austria':
      return 'Áustria'
    case 'ireland':
      return 'Irlanda'
    case 'switzerland':
      return 'Suíça'
    case 'germany':
      return 'Alemanha'
    case 'mexico':
      return 'México'
    case 'sweden':
      return 'Suécia'
    case 'south korea':
      return 'Coreia do Sul'
    case 'argentina':
      return 'Argentina'
    case 'egypt':
      return 'Egito'
    case 'russia':
      return 'Rússia'
    case 'turkey':
      return 'Turquia'
    case 'indonesia':
      return 'Indonésia'
    case 'england':
      return 'Inglaterra'
    case 'chile':
      return 'Chile'

    default:
      return ''
  }
}


